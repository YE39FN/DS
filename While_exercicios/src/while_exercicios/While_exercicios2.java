
package while_exercicios;


public class While_exercicios2 {
        public static void main(String[] args) {
            int numero1 = 0;
            int numero2 = 100;
           for (int contador = numero1; contador <= numero2; contador=contador+2){
                System.out.println(contador);
            } 
           System.out.println("Estes são os números pares de 1 a 100:  ");
        }
    
}
